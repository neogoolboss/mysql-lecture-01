/* ORDER BY */
SELECT
       MENU_CODE
     , MENU_NAME
     , MENU_PRICE
  FROM TBL_MENU
--   ORDER BY MENU_PRICE ASC; -- 오름차순 정렬(안써도 자동으로 됨)
ORDER BY MENU_PRICE; -- ORDER BY를 쓰면 정렬기능

SELECT
       MENU_CODE
     , MENU_NAME
     , MENU_PRICE
  FROM TBL_MENU
ORDER BY MENU_PRICE DESC; -- 내림차순정렬 (꼭 작성해야 함)

SELECT
       MENU_CODE
	 , MENU_NAME
     , MENU_PRICE
  FROM TBL_MENU
ORDER BY MENU_PRICE DESC,
		 MENU_NAME ASC;
         
SELECT
       MENU_CODE
	 , MENU_PRICE
     , MENU_CODE * MENU_PRICE
  FROM TBL_MENU
ORDER BY MENU_CODE * MENU_PRICE DESC;

