-- 오름차순 시 NULL은 처음으로 호출
SELECT
       CATEGORY_CODE
	 , CATEGORY_NAME
     , REF_CATEGORY_CODE
  FROM TBL_CATEGORY
ORDER BY REF_CATEGORY_CODE;

-- 오름차순 시 NULL을 마지막으로 호출
SELECT
       CATEGORY_CODE
	 , CATEGORY_NAME
     , REF_CATEGORY_CODE
  FROM TBL_CATEGORY
ORDER BY REF_CATEGORY_CODE IS NULL; 

-- 내림차순 시 NULL을 마지막으로 호출
SELECT
       CATEGORY_CODE
	 , CATEGORY_NAME
     , REF_CATEGORY_CODE
  FROM TBL_CATEGORY
ORDER BY REF_CATEGORY_CODE DESC;

-- 내림차순 시 NULL을 처음으로 호출
SELECT
       CATEGORY_CODE
	 , CATEGORY_NAME
     , REF_CATEGORY_CODE
  FROM TBL_CATEGORY
ORDER BY REF_CATEGORY_CODE IS NULL DESC, REF_CATEGORY_CODE DESC;